#define SCREEN_WIDTH 128 // OLED display width, in pixels
#define SCREEN_HEIGHT 64 // OLED display height, in pixels

// Declaration for an SSD1306 display connected to I2C (SDA, SCL pins)
#define OLED_RESET 4 // Reset pin # (or -1 if sharing Arduino reset pin)
#define I2C_ADDRESS 0x3C

#define LOGO_WIDTH    128
#define LOGO_HEIGHT   64


static uint32_t previousMillis;
uint8_t lcdUpdateFreq = 5040;

SSD1306AsciiAvrI2c oled;

void setupLcd() {
    oled.begin(&Adafruit128x64, I2C_ADDRESS, OLED_RESET);
    // Call oled.setI2cClock(frequency) to change from the default frequency.

    oled.setFont(Adafruit5x7);

    oled.clear();
    oled.set2X();
    oled.println(F("Hello"));
    oled.println(F(" Shroomies!"));
}

void loopLcd() {
    uint32_t currentMillis = millis();
    if (currentMillis < previousMillis + lcdUpdateFreq)
        return; // do nothing if the minimum update interval hasn't passed

    previousMillis = currentMillis;

    char buf[50];
    myGui->getLCDContent(buf);

    drawText(buf);
}

char delimiters[] = ">";

void drawText(char *buf) {
    char *line;

    oled.clear();
    oled.setCursor(0, 0);

    line = strtok(buf, delimiters);
    while (line != nullptr) {
        Serial.println(line);
        oled.println(line);
        line = strtok(nullptr, delimiters);
    }
}

